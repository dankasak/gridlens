async def async_setup_entry(hass: HomeAssistant, entry: ConfigEntry) -> bool:
    """Set up Grid Lens from a config entry."""
    _LOGGER.info("Setting up Grid Lens")

    hass.data.setdefault(DOMAIN, {})

    coordinator = GridLensCoordinator(hass, entry)
    hass.data[DOMAIN][entry.entry_id] = coordinator

    # Register services
    from .services import async_setup_services
    await async_setup_services(hass, entry)

    await hass.config_entries.async_forward_entry_setups(entry, PLATFORMS)

    # Pre-calculate in the background so the card has data immediately on load.
    hass.async_create_task(coordinator.async_refresh())

    return True


async def async_unload_entry(hass: HomeAssistant, entry: ConfigEntry) -> bool:
    """Unload a config entry."""
    if unload_ok := await hass.config_entries.async_unload_platforms(entry, PLATFORMS):
        hass.data[DOMAIN].pop(entry.entry_id)
    
    return unload_ok


class GridLensCoordinator(DataUpdateCoordinator):
    """Coordinator to manage electricity plan comparison data."""

    def __init__(self, hass: HomeAssistant, entry: ConfigEntry) -> None:
        """Initialize coordinator."""
        super().__init__(
            hass,
            _LOGGER,
            name=DOMAIN,
            update_interval=None,  # Manual refresh only — never auto-calculate
        )
        self.entry = entry
        self.hass = hass
        self.calculator = None  # Will be created on first update

    async def _async_update_data(self):
        """Fetch data from energy sensors and calculate plan comparisons."""
        import aiohttp
        from .plan_calculator import PlanCalculator
        from .const import CONF_GRIDLENS_API_KEY, CONF_GRIDLENS_API_URL, CONF_STATE

        api_key = self.entry.data.get(CONF_GRIDLENS_API_KEY, "")
        api_url = self.entry.data.get(CONF_GRIDLENS_API_URL, "https://api.gridlens.au")
        state   = self.entry.data.get(CONF_STATE, "NSW")

        plan_data: dict = {}
        try:
            import aiohttp as _aiohttp
            _session = async_get_clientsession(self.hass)
            async with _session.get(
                f"{api_url}/plans",
                params={"state": state},
                headers={"X-API-Key": api_key},
                timeout=_aiohttp.ClientTimeout(total=15),
            ) as _r:
                if _r.status == 200:
                    _resp = await _r.json()
                    plan_data = _resp.get("plans", {})
                else:
                    _LOGGER.warning("Coordinator: API /plans returned %s", _r.status)
        except Exception as _exc:
            _LOGGER.warning("Coordinator: could not fetch plan data: %s", _exc)

        self.calculator = PlanCalculator(self.hass, self.entry)
        self.calculator.plan_data = plan_data
        result = await self.calculator.calculate_plan_costs()
        _LOGGER.info(f"Plan calculation complete: {result.get('usage_days', 0)} days")

        # Build per-plan sensor data for CoordinatorEntity sensors
        plan_metrics = {}
        plan_details = result.get("plan_details", {})
        for plan_id, plan_key in PLAN_ID_TO_KEY.items():
            detail = plan_details.get(plan_key, {})
            opt = detail.get("optimization", {})
            schedule = opt.get("schedule", [])

            total_import = opt.get("total_import_kwh", 0.0) or 0.0
            total_export = opt.get("total_export_kwh", 0.0) or 0.0
            total_import_cost = opt.get("total_import_cost", 0.0) or 0.0
            total_export_credit = opt.get("total_export_credit", 0.0) or 0.0
            total_charge = sum(h.get("charge_kwh", 0) for h in schedule)
            total_discharge = sum(h.get("discharge_kwh", 0) for h in schedule)
            net_cost = opt.get("net_cost", detail.get("breakdown", {}).get("total", 0.0)) or 0.0

            plan_metrics[plan_id] = {
                "grid_import": round(total_import, 3),
                "grid_export": round(total_export, 3),
                "battery_charge": round(total_charge, 3),
                "battery_discharge": round(total_discharge, 3),
                "solar_production": 0.0,
                "buy_price": round(total_import_cost / total_import, 4) if total_import else 0.0,
                "sell_price": round(total_export_credit / total_export, 4) if total_export else 0.0,
                "hourly_cost": round(net_cost / len(schedule), 4) if schedule else 0.0,
                "optimization_notes": detail.get("strategy", ""),
            }

        result["plan_metrics"] = plan_metrics
        return result
