def _plan_from_history(entries: list, start_date) -> str | None:
    """Return the plan name active at start_date (latest entry whose date <= start_date)."""
    query = start_date.date().isoformat() if hasattr(start_date, 'date') else str(start_date)[:10]
    active = None
    for entry in sorted(entries, key=lambda e: e['date']):
        if entry['date'] <= query:
            active = entry['plan_name']
    return active


async def async_setup(hass: HomeAssistant, config: dict) -> bool:
    """Set up the Grid Lens component."""
    import pathlib
    import uuid as _uuid
    from homeassistant.helpers.storage import Store
    from homeassistant.const import EVENT_HOMEASSISTANT_STARTED

    # Serve www/ directly from the integration directory
    www_path = str(pathlib.Path(__file__).parent / "www")
    try:
        from homeassistant.components.http import StaticPathConfig
        await hass.http.async_register_static_paths(
            [StaticPathConfig("/grid_lens", www_path, False)]
        )
    except Exception as e:
        _LOGGER.warning("Could not register static path for Grid Lens: %s", e)

    # Register Lovelace resource modules using the runtime collection (like simple_timer)
    # This ensures the frontend is notified immediately and stale entries are cleaned up.
    card_urls = [
        "/grid_lens/cards/grid-lens-card.js",
        "/grid_lens/cards/grid-lens-flow-card.js",
    ]
    stale_urls = {
        "/grid_lens/cards/electricity-plan-comparison-card.js",
        "/grid_lens/cards/electricity-energy-flow-card.js",
        "/local/grid-lens/electricity-energy-flow-card.js",
        "/local/grid-lens/electricity-plan-comparison-card.js",
    }
    try:
        lovelace_data = hass.data.get("lovelace")
        if lovelace_data and hasattr(lovelace_data, "resources"):
            resources = lovelace_data.resources
            await resources.async_get_info()
            # Remove stale entries
            for item in list(resources.async_items()):
                if item.get("url", "").split("?")[0] in stale_urls:
                    try:
                        await resources.async_delete_item(item["id"])
                    except Exception:
                        pass
            # Add new entries if not already present
            existing = {item.get("url", "").split("?")[0] for item in resources.async_items()}
            for url in card_urls:
                if url not in existing:
                    await resources.async_create_item({"res_type": "module", "url": url})
        else:
            # Fallback: write directly to store
            resources_store = Store(hass, 1, "lovelace_resources")
            data = await resources_store.async_load() or {"items": []}
            data["items"] = [i for i in data["items"] if i["url"] not in stale_urls]
            existing_urls = {item["url"] for item in data["items"]}
            for url in card_urls:
                if url not in existing_urls:
                    data["items"].append({"id": str(_uuid.uuid4()), "type": "module", "url": url})
            await resources_store.async_save(data)
    except Exception as e:
        _LOGGER.warning("Could not register Lovelace resources for Grid Lens: %s", e)

    # Auto-register the Grid Lens dashboard in the sidebar
    async def _register_dashboard(_event=None):
        lovelace_data = hass.data.get("lovelace")
        if not lovelace_data or not hasattr(lovelace_data, "dashboards"):
            _LOGGER.warning("Grid Lens: lovelace not available, skipping dashboard registration")
            return
        try:
            dashboards = lovelace_data.dashboards

            dashboard_config = {
                "id": "grid_lens",
                "icon": "mdi:transmission-tower",
                "title": "Grid Lens",
                "url_path": "grid-lens",
                "require_admin": False,
                "mode": "storage",
                "show_in_sidebar": True,
            }

            # Persist to lovelace_dashboards store (read by HA on next startup)
            dash_store = Store(hass, 1, "lovelace_dashboards")
            dash_data = await dash_store.async_load() or {"items": []}
            already_stored = any(d.get("url_path") == "grid-lens" for d in dash_data.get("items", []))
            if not already_stored:
                dash_data.setdefault("items", []).append(dashboard_config)
                await dash_store.async_save(dash_data)

            # Seed dashboard content if not yet created
            content_store = Store(hass, 1, "lovelace.grid_lens")
            if not await content_store.async_load():
                await content_store.async_save({
                    "config": {
                        "title": "Grid Lens",
                        "views": [{
                            "type": "panel",
                            "title": "Plan Comparison",
                            "path": "plan-comparison",
                            "cards": [{
                                "type": "custom:grid-lens-card",
                                "entity": "sensor.grid_lens_amber_monthly_cost",
                            }]
                        }]
                    }
                })

            # If already registered in-memory, nothing more to do
            if isinstance(dashboards, dict) and "grid-lens" in dashboards:
                return
            if not isinstance(dashboards, dict) and any(
                d.get("url_path") == "grid-lens" for d in dashboards.async_items()
            ):
                return

            # In HA 2026 dashboards is a plain dict; try adding the LovelaceStorage
            # object directly so the sidebar updates without a restart.
            if isinstance(dashboards, dict):
                try:
                    from homeassistant.components.lovelace.dashboard import LovelaceStorage as _LS
                    dashboards["grid-lens"] = _LS(hass, dashboard_config)
                    _LOGGER.info("Grid Lens dashboard added to sidebar")
                except Exception:
                    _LOGGER.info("Grid Lens dashboard written to storage — will appear in sidebar after next HA restart")
            else:
                await dashboards.async_create_item(dashboard_config)
                _LOGGER.info("Grid Lens dashboard registered in sidebar")
        except Exception as e:
            _LOGGER.warning("Could not register Grid Lens dashboard: %s", e)

    if hass.is_running:
        hass.async_create_task(_register_dashboard())
    else:
        hass.bus.async_listen_once(EVENT_HOMEASSISTANT_STARTED, _register_dashboard)

