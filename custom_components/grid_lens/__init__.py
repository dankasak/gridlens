    class PlanDataView(HomeAssistantView):
        """Serve plan optimization data for dashboard."""
        
        url = "/api/grid_lens/plan_data"
        name = "api:grid_lens:plan_data"
        requires_auth = False
        
        def __init__(self, hass_instance):
            self.hass = hass_instance
        
        async def get(self, request):
            """Get plan optimization data with optional date range."""
            import json
            from datetime import datetime

            # Parse dates in the HA configured timezone so "May 12 00:00" means
            # local midnight, not UTC midnight (which would be wrong for AEST).
            try:
                from zoneinfo import ZoneInfo
            except ImportError:
                from backports.zoneinfo import ZoneInfo
            ha_tz = ZoneInfo(self.hass.config.time_zone or "UTC")

            start_date_str = request.query.get('start_date')
            end_date_str = request.query.get('end_date')
            start_date = None
            end_date = None

            if start_date_str:
                try:
                    start_date = datetime.fromisoformat(
                        start_date_str.replace('Z', '+00:00')
                    )
                    if start_date.tzinfo is None:
                        start_date = start_date.replace(tzinfo=ha_tz)
                except ValueError:
                    return web.Response(
                        text=json.dumps({'error': 'Invalid start_date format'}),
                        content_type='application/json',
                        status=400
                    )

            if end_date_str:
                try:
                    end_date = datetime.fromisoformat(
                        end_date_str.replace('Z', '+00:00')
                    )
                    if end_date.tzinfo is None:
                        end_date = end_date.replace(tzinfo=ha_tz)
                except ValueError:
                    return web.Response(
                        text=json.dumps({'error': 'Invalid end_date format'}),
                        content_type='application/json',
                        status=400
                    )

            entries = self.hass.config_entries.async_entries(DOMAIN)
            if not entries:
                return web.Response(
                    text=json.dumps({'error': 'Integration not loaded'}),
                    content_type='application/json',
                    status=404
                )

            coordinator = self.hass.data.get(DOMAIN, {}).get(entries[0].entry_id)
            if not coordinator:
                return web.Response(
                    text=json.dumps({'error': 'No coordinator'}),
                    content_type='application/json',
                    status=404
                )

            # Custom date range: recalculate on-the-fly and return directly.
            if start_date or end_date:
                import aiohttp as _aiohttp
                from .plan_calculator import PlanCalculator
                from .const import CONF_GRIDLENS_API_KEY, CONF_GRIDLENS_API_URL, CONF_STATE
                from homeassistant.helpers.storage import Store
                entries = self.hass.config_entries.async_entries(DOMAIN)
                entry_obj = entries[0] if entries else None
                if not entry_obj:
                    return web.Response(text=json.dumps({'error': 'No entry'}), content_type='application/json', status=404)
                _api_key = entry_obj.data.get(CONF_GRIDLENS_API_KEY, "")
                _api_url = entry_obj.data.get(CONF_GRIDLENS_API_URL, "https://api.gridlens.au")
                _state   = entry_obj.data.get(CONF_STATE, "NSW")
                _plan_data: dict = {}
                try:
                    _sess = async_get_clientsession(self.hass)
                    async with _sess.get(f"{_api_url}/plans", params={"state": _state},
                                         headers={"X-API-Key": _api_key, "User-Agent": "GridLens-HA-Integration/1.0"},
                                         timeout=_aiohttp.ClientTimeout(total=15)) as _r:
                        if _r.status == 200:
                            _plan_data = (await _r.json()).get("plans", {})
                except Exception as _exc:
                    _LOGGER.warning("plan-rates: could not fetch plan data: %s", _exc)
                calculator = PlanCalculator(self.hass, entry_obj)
                calculator.plan_data = _plan_data

                # Derive which plan the user was actually on at start_date from change history.
                if start_date:
                    _hist_store = Store(self.hass, _HISTORY_STORAGE_VERSION, _HISTORY_STORAGE_KEY)
                    _hist_data = await _hist_store.async_load() or {"entries": []}
                    _hist_plan = _plan_from_history(_hist_data["entries"], start_date)
                    if _hist_plan:
                        calculator.current_plan_override = _hist_plan
                        _LOGGER.info("History: current plan at %s is %s", start_date.date(), _hist_plan)

                response_data = await calculator.calculate_plan_costs(start_date, end_date)
                _LOGGER.info(f"Custom date range calculation complete: {response_data.get('usage_days')} days")
                return web.Response(
                    text=json.dumps(response_data),
                    content_type='application/json'
                )

            # Default: use coordinator cached data (populated on startup).
            if not coordinator.data:
                return web.Response(
                    text=json.dumps({'status': 'calculating'}),
                    content_type='application/json',
                    status=202
                )

            plan_details = coordinator.data.get('plan_details', {})
            response_data = {
                'plan_details': plan_details,
                'energy_flows': coordinator.data.get('energy_flows', {}),
                'amber_total': coordinator.data.get('amber_total', 0),
                'current_plan_name': coordinator.data.get('current_plan_name'),
                'deferrable_devices': coordinator.data.get('deferrable_devices', []),
                'usage_days': coordinator.data.get('usage_days', 0),
                'start_date': coordinator.data.get('start_date', ''),
                'end_date': coordinator.data.get('end_date', ''),
                'calculation_date': coordinator.data.get('calculation_date', ''),
            }

            return web.Response(
                text=json.dumps(response_data),
                content_type='application/json'
            )
    
